package com.zocarro.myvideobook.Model;

public class feedlinkmodel {

    String id,link,title,date;

    public feedlinkmodel(String id, String link, String title, String date) {
        this.id = id;
        this.link = link;
        this.title = title;
        this.date = date;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
